
/// <summary>
/// This enumeration provides privacy options for the account class.
/// </summary>
public enum PrivacyLevel
{
    /// <summary>
    /// The account is visible only for the owner.
    /// </summary>
    Private = 1,
    /// <summary>
    ///  The account is visible for everyone.
    /// </summary>
    Public = 2,
    /// <summary>
    /// The account is visible only for registered participants.
    /// </summary>
    Registered = 3
}